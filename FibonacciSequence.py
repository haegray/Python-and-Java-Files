# This is a program that will list the Fibonacci numbers.

def main():
    print("This program will list the Fibonacci numbers to the amount of numbers you wish to see.")

    n=eval(input("Please input the amount of numbers you would like the list to populate."))
    x = 1
    y = 1

    for i in range(n):
        x <= n
        z = x + y

        print(x)
        x = y
        y = z

main()
